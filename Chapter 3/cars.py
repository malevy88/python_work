cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort() #changes order of list to alphabetical order
print(cars)

cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort(reverse=True) #changes order of list to reverse alphbetical order
print(cars)

cars = ['bmw', 'audi', 'toyota', 'subaru']
print("Here is the original list")
print(cars)

print("\nHere is the sorted list:")
