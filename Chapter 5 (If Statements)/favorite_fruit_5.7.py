favorite_fruits = ['cherries', 'plums', 'grapes']

if 'bananas' in favorite_fruits:
    print("You must really love bananas.")
if 'cherries' in favorite_fruits:
    print("I thought I remembered you liking cherries.")
if 'apples' in favorite_fruits:
    print("I didn't think you liked apples so much.")
if 'grapes' in favorite_fruits:
    print("You really like those grapes huh?")
if 'plums' in favorite_fruits:
    print("Plums are my favorite as well.")
